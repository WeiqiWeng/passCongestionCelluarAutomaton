function ff = my_fourier1(t)
a0 = 6.282;
w = 0.2639;
a = 0.1*[-30.29,-1.714,10.39,-2.697,-4.69,2.477,1.358,-3.18];
b = 0.1*[-30.54,-13.28,2.038,2.257,-5.624 ,-3.145,8.028,-3.692];
x(1) = a0;
x(2:2:16) = a;
x(3:2:17) = b;
ff = x(1) + x(2)*cos(t*w) + x(3)*sin(t*w) + x(4)*cos(2*t*w) + x(5)*sin(2*t*w) + x(6)*cos(3*t*w)...
   + x(7)*sin(3*t*w) + x(8)*cos(4*t*w) + x(9)*sin(4*t*w) + x(10)*cos(5*t*w) + x(11)*sin(5*t*w)...
   + x(12)*cos(6*t*w) + x(13)*sin(6*t*w) + x(14)*cos(7*t*w) + x(15)*sin(7*t*w) + x(16)*cos(8*t*w) + x(17)*sin(8*t*w);
end
